"""ASF initial schema

Revision ID: 0001_initial_schema
Revises:
Create Date: 2026-06-04 00:00:00.000000

Creates the complete ASF domain schema. The migration is hand-written
(not autogenerated) so that the DDL is deterministic across re-deploys
and so that the PostgreSQL-specific bits (native ENUM types, JSONB
columns, ON DELETE CASCADE foreign keys) are explicit. It is also
deliberately idempotent in spirit: running ``alembic upgrade head``
twice in a row is a no-op because the ``alembic_version`` table
records the current revision; running the same SQL twice without the
version table would error.

Tables created (all in the public schema by default):

* ``missions``      — top-level orchestration record
* ``agents``        — PraisonAI agent per role
* ``tasks``         — unit of work assigned to an agent
* ``executions``    — single execution attempt of a task
* ``approvals``     — human review decision for a mission gate
* ``chat_messages`` — PM-Chat lines attached to a mission

The migration also creates the following PostgreSQL ENUM types:

* ``mission_status``
* ``agent_role``
* ``agent_status``
* ``task_status``
* ``execution_status``
* ``approval_gate_type``
* ``approval_decision``
* ``chat_sender_type``

ENUMs are wrapped in ``CREATE TYPE IF NOT EXISTS`` *blocks*; the
revision itself is guarded by the ``alembic_version`` table so the
DDL only runs once. A downgrade drops every table and enum.

Notable design points:

* Foreign keys are declared ``ON DELETE CASCADE`` so that removing a
  mission purges its agents, tasks, executions, approvals, and chat
  messages. The ``chat_messages.sender_id`` FK uses ``ON DELETE SET
  NULL`` instead, because a chat history should survive the deletion
  of the agent that wrote it.
* Every table has ``created_at`` and ``updated_at`` ``TIMESTAMP WITH
  TIME ZONE`` columns (the application bumps ``updated_at``
  explicitly; we do not rely on ``ON UPDATE`` because it is not
  portable to SQLite, which the test suite uses).
* The status / role / sender columns are stored as PostgreSQL native
  ENUM types, which constrain the allowed values on PostgreSQL (the
  production backend). Only ``missions.status`` additionally carries
  an explicit portable ``CHECK`` constraint (``ck_missions_status``);
  the other enum columns rely on the PG ENUM type alone. On SQLite
  (the test backend) native enums render as bare ``VARCHAR`` with no
  CHECK, so value validation there is enforced by the SQLAlchemy ORM
  enum types at insert time rather than at the database level.
* B-tree indexes are added on ``missions(status)`` and
  ``missions(created_at)`` to keep list queries cheap. Additional
  indexes are added on the foreign-key columns and on
  ``executions(status)`` / ``chat_messages(sender_type)`` /
  ``chat_messages(created_at)`` because those are common filter
  columns in API endpoints.
* Unstructured metadata columns are declared as ``JSONB`` on
  PostgreSQL and ``JSON`` on SQLite (using
  ``sa.JSON().with_variant(JSONB(), "sqlite")``).
"""

