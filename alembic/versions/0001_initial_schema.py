def _jsonb_column() -> sa.types.TypeEngine:
    """Return a ``JSONB`` column on PostgreSQL, ``JSON`` elsewhere.

    Using a helper keeps the migration readable and makes the variant
    policy explicit in one place.
    """
    return postgresql.JSONB().with_variant(sa.JSON(), "sqlite")


def _uuid_column() -> sa.types.TypeEngine:
    """Return a UUID column on PostgreSQL, CHAR(36) on SQLite.

    SQLAlchemy's ``UUID`` type on SQLite falls back to ``CHAR(36)`` so
    the same model can be used in unit tests without a running
    PostgreSQL instance.
    """
    return postgresql.UUID(as_uuid=True).with_variant(sa.String(36), "sqlite")


